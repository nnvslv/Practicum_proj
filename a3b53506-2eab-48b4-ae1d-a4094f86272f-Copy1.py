# In[41]:


def SMAPE(y_test, y_perd):
    return 100/len(y_test) * np.sum(2 * np.abs(y_perd - y_test) / (np.abs(y_test) + np.abs(y_perd)))


# In[42]:


score = make_scorer(SMAPE, greater_is_better=False)


